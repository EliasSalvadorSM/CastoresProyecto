    void limpiar(){
        for(int i = 0; i< modelo.getRowCount();i++){
            modelo.removeRow(i);
            i=0-1;
        }
    }
    
    private void listar(){
        List<producto> lista=ep.ListarSalidas();
        modelo =  (DefaultTableModel) tblSalidas.getModel();
        Object[] ob=new Object[3];
        for(int i =0; i<lista.size();i++){
            ob[0]= lista.get(i).getIdProducto();
            ob[1]= lista.get(i).getNombreProducto();
            ob[2]= lista.get(i).getCantidad();
            modelo.addRow(ob);
        }
        tblSalidas.setModel(modelo);
    }

    
